// Resuelve un caso de prueba, leyendo de la entrada la
// configuración, y escribiendo la respuesta
bool resuelveCaso() {
    // leer los datos de la entrada
	int n;
	std::cin >> n;
    if (n == 0)
        return false;
    
	colaExtendida<horas> q;
	for (int i = 0; i < n; ++i) {
		horas lectura;
		std::cin >> lectura;
		q.push(lectura);
	}
    
	q.eliminarElementosPares();
	q.mostrarNodos();
    
    return true;
    
}

